#include "3-calc.h"

/**
 * op_add - a function that returns the sum of a and b.
 * @a: first  input
 * @b: second input
 * Return: sum
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - a function that returns the subtract of a and b.
 * @a: first input
 * @b: second input
 * Return: sub
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - a function that returns the multiply of a and b
 * @a: first function
 * @b: second function
 * Return: mul
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - a function that returns the divide of a and b
 * @a: first function
 * @b: second function
 * Return: div
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - a function that returns the module of a and b
 * @a: first function
 * @b: second function
 * Return: mod
 */
int op_mod(int a, int b)
{
	return (a % b);
}
